export function assignWords(playerIds: string[]): Record<string, { word: string; category: string }> {
  const cats = Object.keys(YS_CATEGORIES)
  const cat = cats[Math.floor(Math.random() * cats.length)]
  const pool = [...YS_CATEGORIES[cat]].sort(() => Math.random() - 0.5)
  const result: Record<string, { word: string; category: string }> = {}
  playerIds.forEach((id, i) => { result[id] = { word: pool[i % pool.length], category: cat } })
  return result
}
